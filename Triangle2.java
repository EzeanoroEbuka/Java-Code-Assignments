 public class Triangle2 {
 public static void main(String[] args) {
	int n = 5;
		
	for(int k =1;k <= n;k++){
		for(int i = 1;i <= k;i++){
			System.out.print(" ");}
		for(int j = n;j >= k;j--){
	 	    System.out.print("*");}
				
		System.out.println();}

   

 }
}



